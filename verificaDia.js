// Programa Verificador do Dia da Semana

// 1. Criar uma variável para armazenar o nome do dia
let dia = "Sábado"; // Você pode alterar para outro dia, como "Terça", "Domingo", etc.

// 2. Verificar se é final de semana ou dia de semana
